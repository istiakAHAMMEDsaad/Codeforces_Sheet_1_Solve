/*
int : 32 Bit integer.
long long : 64 bit integer
Char : 8 bit Characters & symbols
Float : 32 bit real value
Double : 64 bit real value
*/
#include <iostream>
using namespace std;
int main()
{
    int a;
    long long b;
    char c;
    float d;
    double e;
    cin >> a >> b >> c >> d >> e;
    cout << a << "\n"
         << b << "\n"
         << c << "\n"
         << d << "\n"
         << e;
    return 0;
}

